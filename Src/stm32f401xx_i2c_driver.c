//send and recieve data apis

void I2C_MasterSendData(I2C_Handle_t *pI2CHandle,uint8_t *pTxBuffer, uint32_t Len, uint8_t SlaveAddr)
{
	//Gnerate the start condition
	I2C_GenerateStartCondition(pI2CHandle->pI2Cx);

	//confirm that the start condition is generated by checking the sb flag in sr1
	//note: until sb is cleared scl is pulled to low
	while(!(pI2CHandle->pI2Cx->SR1&0x1));

	// send address of the slave with r/nw bit set to w(0) (total 8 bits)
	I2C_ExecuteAddressPhase(pI2CHandle->pI2Cx,SlaveAddr,I2C_WRITE);

	//confirm that address phase is completed by checking the ADDR flag in SR1
	while(!(pI2CHandle->pI2Cx->SR1&0x2));

	//clear the addr flag according to its software sequence
	//NOTE: until addr is scl will be stretched
	I2C_ClearAddrFlag(pI2CHandle);

	//send the data until len becomes 0
	while(Len > 0)
	{
		while(!((pI2CHandle->pI2Cx->SR1>>7)&0x1));
		pI2CHandle->pI2Cx->DR = *pTxBuffer;
		pTxBuffer++;
		Len--;
	}

	//When len becomes 0 wait for txe =1 and btf =1 before generating stop condition
	//note: txe =1 and btf =1 means both sr and dr are empty and next tx should begin
	//when btf =1 scl is pulled to low
	//wait till txe set
	while(!((pI2CHandle->pI2Cx->SR1>>7)&0x1));
	//wait till btf is set
	while(!((pI2CHandle->pI2Cx->SR1>>2)&0x1));


	//generate stop condition and master need not to wait for completion of stop condition
	//note: generating stop , automatically clears the btf
	I2C_GenerateStopCondition(pI2CHandle->pI2Cx);


}

void I2C_MasterRecieveData(I2C_Handle_t *pI2CHandle,uint8_t *pRxBuffer, uint32_t Len, uint8_t SlaveAddr)
{
	// Generate the start condition
	I2C_GenerateStartCondition(pI2CHandle->pI2Cx);


	//confirm that the start generation completed by checking the SB flag in SR1
	//until SB IS Cleared scl is pulled to gnd
	while(!(pI2CHandle->pI2Cx->SR1&0x1));

	//send the address of slave with r/nw set to 1
	I2C_ExecuteAddressPhase(pI2CHandle->pI2Cx, SlaveAddr, I2C_READ);


	// wait until addr phase is completed by checking the addr flag in SR1
	while(!(pI2CHandle->pI2Cx->SR1&0x2));

	//procedure to read 1 byte data from slave
	if(Len ==1)
	{
		//Disable Acking
		pI2CHandle->pI2Cx->CR1	&= ~(1<<10);

		//clear the addr flag
		I2C_ClearAddrFlag(pI2CHandle);

		//wait until rxne becomes 1
		while(!((pI2CHandle->pI2Cx->SR1>>6)&0x1));

		//generate stop condition
		I2C_GenerateStopCondition(pI2CHandle->pI2Cx);

		//read data into buffer
		*pRxBuffer = pI2CHandle->pI2Cx->DR;

		//set the ack bit
		pI2CHandle->pI2Cx->CR1	|= (1<<10);

		return;

	}

	if(Len>1)
	{
		//clear the addr flag
		I2C_ClearAddrFlag(pI2CHandle);

		//read the data until len becomes zero
		while(Len >0)
		{
			//WAIT UNTIL RXNE BECOMES 1
			while(!((pI2CHandle->pI2Cx->SR1>>6)&0x1));

			if(Len==2)
			{
				//wait until btf is set
				while(!((pI2CHandle->pI2Cx->SR1>>2)&0x1));

				//clear the ack bit
				pI2CHandle->pI2Cx->CR1	&= ~(1<<10);

				//generate stop condition
				I2C_GenerateStopCondition(pI2CHandle->pI2Cx);

			}

			//read the data from data register in to buffer
			*pRxBuffer = pI2CHandle->pI2Cx->DR;
			Len--;
			//increment the buffer address
			pRxBuffer++;



		}
	}
	// re- enable acking
	pI2CHandle->pI2Cx->CR1	|= (1<<10);




}


