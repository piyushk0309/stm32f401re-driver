//helper functions


static void I2C_GenerateStartCondition(I2C_Reg_Def_t *pI2Cx)
{
	pI2Cx->CR1	|= (1<<8);



}

static void I2C_ExecuteAddressPhase(I2C_Reg_Def_t *pI2Cx,uint8_t Addr,uint8_t WorR)
{
	Addr = Addr<<1;
	if(WorR==I2C_WRITE)
	{
		Addr &= ~(1);
	}
	else
	{
		Addr |=1;
	}
	pI2Cx->DR = Addr;
}

static void I2C_ClearAddrFlag(I2C_Handle_t *pI2CHandle)
{
	uint32_t dummyread;
	//check for device mode
	if(pI2CHandle->pI2Cx->SR2&(0x1))
	{
		//device is in master mode
		if(pI2CHandle->TxRxState==I2C_BUSY_IN_RX)
		{
			if(pI2CHandle->RxSize==1)
			{
				//first disable the ack
				pI2CHandle->pI2Cx->CR1 &= ~(1<<10);

			}
		//clear the addr flag (read sr1 and sr2)
		dummyread = pI2CHandle->pI2Cx->SR1;
		dummyread = pI2CHandle->pI2Cx->SR1;
		(void)dummyread;

		}
		else
		{
			//clear the addr flag (read sr1 and sr2)
			dummyread = pI2CHandle->pI2Cx->SR1;
			dummyread = pI2CHandle->pI2Cx->SR1;
			(void)dummyread;


		}
	}
	else
	{
		//device in slave mode
		//clear the addr flag (read sr1 and sr2)
		dummyread = pI2CHandle->pI2Cx->SR1;
		dummyread = pI2CHandle->pI2Cx->SR1;
		(void)dummyread;
	}


}
static void I2C_GenerateStopCondition(I2C_Reg_Def_t *pI2Cx)
{
	pI2Cx->CR1	|= (1<<9);



}


/*********************************************************************
 * @fn      		  - I2C_IRQConfig
 *
 * @brief             - interrupt request configuration of that I2C port
 *
 * @param[in]         - irq number of that interrupt
 * @param[in]         - priority number of that interrupt
 * @param[in]         - enable or disable macros
 *
 * @return            -  none
 *
 * @Note              -  none

 */

void I2C_IRQInterruptConfig(uint8_t IRQNumber, uint8_t EnorDi)
{

	if(EnorDi == ENABLE)
	{
	if(IRQNumber <= 31)
	{
		//program iser0 register
		*NVIC_ISER0 |= (1 << IRQNumber);
	}
	else if (IRQNumber >31 && IRQNumber <64)
	{
		//program iser1 register
		*NVIC_ISER1 |= (1 << (IRQNumber%32));

	}
	else if(IRQNumber >= 64 && IRQNumber < 96)
	{
		//program iser2 register
		*NVIC_ISER2 |= (1 << (IRQNumber%64));
	}
	}

	else
	{

		if(IRQNumber <= 31)
			{
				//program icer0 register
			*NVIC_ICER0 |= (1 << IRQNumber);

			}
			else if (IRQNumber >31 && IRQNumber <64)
			{
				//program icer1 register
				*NVIC_ICER1 |= (1 << (IRQNumber%32));
			}
			else if(IRQNumber >= 64 && IRQNumber < 96)
			{
				//program icer2 register
				*NVIC_ICER2 |= (1 << (IRQNumber%64));
			}

	}

}


void  I2C_IRQPriorityConfig(uint8_t IRQNumber,uint32_t Priority )
{
	// first lets find out priority register
	uint8_t iprx = IRQNumber/4;
	uint8_t iprx_section = IRQNumber %4;

	*(NVIC_PR_BASE_ADDR +(iprx)) |= (Priority << (8*iprx_section+4)); // FOR ARM CROTEX M4 ONLY AS LOWER 4 BITS ARE NOT APPLICABLE


}

/*********************************************************************
 * @fn      		  - I2C_MasterSendDataIT
 *
 * @brief             -
 *
 * @param[in]         -
 * @param[in]         -
 * @param[in]         -
 *
 * @return            -
 *
 * @Note              -  Complete the below code . Also include the function prototype in header file

 */
uint8_t  I2C_MasterSendDataIT(I2C_Handle_t *pI2CHandle,uint8_t *pTxBuffer, uint32_t Len,uint8_t SlaveAddr,uint8_t Sr)
{

	uint8_t busystate = pI2CHandle->TxRxState;

	if( (busystate != I2C_BUSY_IN_TX) && (busystate != I2C_BUSY_IN_RX))
	{
		pI2CHandle->pTxBuffer = pTxBuffer;
		pI2CHandle->TxLen = Len;
		pI2CHandle->TxRxState = I2C_BUSY_IN_TX;
		pI2CHandle->DevAddr = SlaveAddr;
		pI2CHandle->Sr = Sr;

		//Implement code to Generate START Condition
		I2C_GenerateStartCondition(pI2CHandle->pI2Cx);

		//Implement the code to enable ITBUFEN Control Bit
		pI2CHandle->pI2Cx->CR2 |= ( 1 << 10);

		//Implement the code to enable ITEVFEN Control Bit
		pI2CHandle->pI2Cx->CR2 |= ( 1 << 9);

		//Implement the code to enable ITERREN Control Bit
		pI2CHandle->pI2Cx->CR2 |= ( 1 << 8);




	}

	return busystate;

}

