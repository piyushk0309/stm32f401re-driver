void I2C_EV_IRQHandling(I2C_Handle_t *pI2CHandle)
{
	//Interrupt handling for both master and slave mode of a device
	uint32_t temp1,temp2,temp3;

	temp1	= pI2CHandle->pI2Cx->CR2&(1<<9);
	temp2	= pI2CHandle->pI2Cx->CR2&(1<<10);

	//1. handle for interrupt generated  by SB event
	// SB only applicable in Master mode
	temp3 	= pI2CHandle->pI2Cx->SR1&(1);

	if(temp1&&temp2)
	{
		//SB flag is set
		//execution of ADDRESS PHASE

		if(pI2CHandle->TxRxState == I2C_BUSY_IN_TX)
		{
			I2C_ExecuteAddressPhase(pI2CHandle->pI2Cx, pI2CHandle->DevAddr, I2C_WRITE);
		}
		else if(pI2CHandle->TxRxState == I2C_BUSY_IN_RX)
		{
			I2C_ExecuteAddressPhase(pI2CHandle->pI2Cx, pI2CHandle->DevAddr, I2C_READ);

		}

	}


	//2.Handle for interrupt generated by ADDR event
	// in master mode when address is sent
	// in slave mode when address is matched
	temp3 = pI2CHandle->pI2Cx->SR2&(1<<1);

	if(temp1&&temp2)
	{
		//ADDR FLAG IS SET

		I2C_ClearAddrFlag(pI2CHandle);
	}

	//3.Handle for event generated by BTF  event
	temp3 = pI2CHandle->pI2Cx->SR2&(1<<2);

	if(temp1&&temp3)
	{
		//BTF FLAG IS SET
		if(pI2CHandle->TxRxState == I2C_BUSY_IN_TX)
		{
			if(pI2CHandle->pI2Cx->SR1&(1<<7))
			{
				// both BTF and TXE set

				if(pI2CHandle->TxLen==0)
				{
				//1. generate stop condition
					if(pI2CHandle->Sr == I2C_DISABLE_SR)
					{I2C_GenerateStopCondition(pI2CHandle->pI2Cx);}

					//2. reset all member elements of the handle structure
					I2C_CloseSendData(pI2CHandle);

					//3. notify the application  about tx complete
					I2C_ApplicationEventCallback(pI2CHandle, I2C_EV_TX_CMPLT);
					}


			}
		}
	}

	//4. Handle for event generated by STOPF event
	// only valid in slave mode
	temp3 = pI2CHandle->pI2Cx->SR2&(1<<4);

	if(temp1&&temp3)
	{
		//STOPF FLAG IS SET
		//cleared by reading SR1 and then CR1
		pI2CHandle->pI2Cx->CR1 |=0x0000;

		//notify the application that stop is generated by the master
		I2C_ApplicationEventCallback(pI2CHandle,I2C_EV_STOP);
	}

	//5. Handle for event generated by TXE event
	temp3 = pI2CHandle->pI2Cx->SR2&(1<<7);

	if(temp1&&temp3&&temp2)
	{
		//check for device mode
		if(pI2CHandle->pI2Cx->SR2&(1<<0))
		{
		//TXE FLAG IS SET
		//WE HAVE TO DO THE DATA TRANSMISSION
		if(pI2CHandle->TxRxState == I2C_BUSY_IN_TX)
		{
			I2C_MasterHandleTXEInterrupt(pI2CHandle);
		}
		}
		else
		{
			//slave
			//make sure slave is in tx mode
			if(pI2CHandle->pI2Cx->SR2&(1<<3))
			{
				I2C_ApplicationEventCallback(pI2CHandle, I2C_EV_DATA_REQ);
			}
		}
	}

	//5. Handle for event generated by TXE event
	temp3 = pI2CHandle->pI2Cx->SR2&(1<<6);

	if(temp1&&temp3&&temp2)
	{
		//check if device master
		if(pI2CHandle->pI2Cx->SR2&(1<<0))
		{
		//RXXE FLAG IS SET
		//WE HAVE TO DO THE DATA TRANSMISSION
		if(pI2CHandle->TxRxState == I2C_BUSY_IN_RX)
		{
			I2C_MasterHandleRXNEInterrupt(pI2CHandle);
		}
		}
		else
		{
			//slave
			//make sure the slave in rx mode
			if(!(pI2CHandle->pI2Cx->SR2&(1<<3)))
			{
				I2C_ApplicationEventCallback(pI2CHandle, I2C_EV_DATA_RCV);
			}


		}




	}
}


static void I2C_MasterHandleRXNEInterrupt(I2C_Handle_t *pI2CHandle)
{
	// WE HAVE TO DO DATA RECEPTION
	if(pI2CHandle->RxSize==1)
	{
		*pI2CHandle->pRxBuffer = pI2CHandle->pI2Cx->DR;
		pI2CHandle->pRxBuffer++;
		pI2CHandle->RxLen--;
	}

	if(pI2CHandle->RxSize >1)
	{
		if(pI2CHandle->RxLen==2)
		{
			//clear the ack bit
			pI2CHandle->pI2Cx->CR1	&= ~(1<<10);


			//read the dr
			pI2CHandle->pRxBuffer = (uint8_t*)pI2CHandle->pI2Cx->DR;
			pI2CHandle->pRxBuffer++;
			pI2CHandle->RxLen--;
		}


	}
	if(pI2CHandle->RxLen==0)
	{
		//close the i2c data reception and notify the application

		//1. generate the stop condition
		I2C_GenerateStopCondition(pI2CHandle->pI2Cx);

		//2.close the i2c rx
		I2C_CloseRecieveData(pI2CHandle);

		//3. notify the application
		I2C_ApplicationEventCallback(pI2CHandle, I2C_EV_RX_CMPLT);

	}
}
static void I2C_MasterHandleTXEInterrupt(I2C_Handle_t *pI2CHandle)
{
	//check for device mode
	if(pI2CHandle->pI2Cx->SR2&(0x1))
	{

		if(pI2CHandle->TxLen >0)
		{
			//1. load the data in dr
			pI2CHandle->pI2Cx->DR =	*(pI2CHandle->pTxBuffer);

			//2. decrement the tx len
			pI2CHandle->TxLen--;

			//3. increment the buffer addr
			pI2CHandle->pTxBuffer++;
		}
	}
}

void I2C_CloseSendData(I2C_Handle_t *pI2CHandle)
{
	pI2CHandle->pTxBuffer = NULL;
	pI2CHandle->TxLen = 0;
	pI2CHandle->TxRxState = I2C_READY;




	//Implement the code to DISABLE ITBUFEN Control Bit
	pI2CHandle->pI2Cx->CR2 &= ~( 1 << 10);

	//Implement the code to DISABLE ITEVFEN Control Bit
	pI2CHandle->pI2Cx->CR2 &= ~( 1 << 9);


}


void I2C_CloseRecieveData(I2C_Handle_t *pI2CHandle)
{
	pI2CHandle->pRxBuffer = NULL;
	pI2CHandle->RxLen = 0;
	pI2CHandle->TxRxState = I2C_READY;
	pI2CHandle->RxSize = 0; //Rxsize is used in the ISR code to manage the data reception





	//Implement the code to enable ITBUFEN Control Bit
	pI2CHandle->pI2Cx->CR2 &= ~( 1 << 10);


	//Implement the code to enable ITEVFEN Control Bit
	pI2CHandle->pI2Cx->CR2 &= ~( 1 << 9);
	// re- enable acking
	pI2CHandle->pI2Cx->CR1	|= (1<<10);

}






/*********************************************************************
 * @fn      		  - I2C_ER_IRQHandling
 *
 * @brief             -
 *
 * @param[in]         -
 * @param[in]         -
 * @param[in]         -
 *
 * @return            -
 *
 * @Note              - Complete the code also define these macros in the driver
						header file
						#define I2C_ERROR_BERR  3
						#define I2C_ERROR_ARLO  4
						#define I2C_ERROR_AF    5
						#define I2C_ERROR_OVR   6
						#define I2C_ERROR_TIMEOUT 7

 */

void I2C_ER_IRQHandling(I2C_Handle_t *pI2CHandle)
{

	uint32_t temp1,temp2;

    //Know the status of  ITERREN control bit in the CR2
	temp2 = (pI2CHandle->pI2Cx->CR2) & ( 1 << 8);


/***********************Check for Bus error************************************/
	temp1 = (pI2CHandle->pI2Cx->SR1) & ( 1<< 8);
	if(temp1  && temp2 )
	{
		//This is Bus error

		//Implement the code to clear the buss error flag
		pI2CHandle->pI2Cx->SR1 &= ~( 1 << 8);

		//Implement the code to notify the application about the error
	   I2C_ApplicationEventCallback(pI2CHandle,I2C_ERROR_BERR);
	}

/***********************Check for arbitration lost error************************************/
	temp1 = (pI2CHandle->pI2Cx->SR1) & ( 1 << 9 );
	if(temp1  && temp2)
	{
		//This is arbitration lost error
		//Implement the code to clear the arbitration lost error flag
		pI2CHandle->pI2Cx->SR1 &= ~( 1 << 9);

		//Implement the code to notify the application about the error
		I2C_ApplicationEventCallback(pI2CHandle,I2C_ERROR_ARLO);

	}

/***********************Check for ACK failure  error************************************/

	temp1 = (pI2CHandle->pI2Cx->SR1) & ( 1 << 10);
	if(temp1  && temp2)
	{
		//This is ACK failure error

	    //Implement the code to clear the ACK failure error flag
		pI2CHandle->pI2Cx->SR1 &= ~( 1 << 10);
		//Implement the code to notify the application about the error
		I2C_ApplicationEventCallback(pI2CHandle,I2C_ERROR_AF);
	}

/***********************Check for Overrun/underrun error************************************/
	temp1 = (pI2CHandle->pI2Cx->SR1) & ( 1<<11);
	if(temp1  && temp2)
	{
		//This is Overrun/underrun

	    //Implement the code to clear the Overrun/underrun error flag
		pI2CHandle->pI2Cx->SR1 &= ~( 1 << 11);

		//Implement the code to notify the application about the error
		I2C_ApplicationEventCallback(pI2CHandle,I2C_ERROR_OVR);

	}

/***********************Check for Time out error************************************/
	temp1 = (pI2CHandle->pI2Cx->SR1) & ( 1 << 14);
	if(temp1  && temp2)
	{
		//This is Time out error

	    //Implement the code to clear the Time out error flag
		pI2CHandle->pI2Cx->SR1 &= ~( 1 << 14);
		//Implement the code to notify the application about the error
		I2C_ApplicationEventCallback(pI2CHandle,I2C_ERROR_TIMEOUT);
	}

}

