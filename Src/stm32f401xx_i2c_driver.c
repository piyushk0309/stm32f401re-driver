/*********************************************************************
 * @fn      		  - I2C_PeriClockControl
 *
 * @brief             - This function enables or disables peripheral clock for the given I2C port
 *
 * @param[in]         - base address of the I2C peripheral
 * @param[in]         - ENABLE or DISABLE macros
 * @param[in]         -
 *
 * @return            -  none
 *
 * @Note              -  none

 */


void I2C_PeriClockControl(I2C_Reg_Def_t *pI2Cx,uint8_t EnorDi)
{
	if(EnorDi == ENABLE)
	{
		if(pI2Cx == I2C1)
		{
			I2C1_PCLK_EN();
		}
		else if(pI2Cx == I2C2)
		{
			I2C2_PCLK_EN();
		}
		else if(pI2Cx == I2C3)
		{
			I2C3_PCLK_EN();
		}

	}
	else
	{
		if(pI2Cx == I2C1)
		{
			I2C1_PCLK_DI();
		}
		else if(pI2Cx == I2C2)
		{
			I2C2_PCLK_DI();
		}
		else if(pI2Cx == I2C3)
		{
			I2C3_PCLK_DI();
		}

	}


}


uint16_t AHB_PreScaler[8] = {2,4,8,16,64,128,256,512};
uint16_t APB_PreScaler[4] = {2,4,8,16};

uint32_t RCC_GetPCLK1Value(void)
{
	uint32_t pclk,SystemClk;
	uint8_t clksrc,temp,apb1p;
	uint16_t ahbp;
	clksrc = (RCC->CFGR>>2)&0x3;

	if(clksrc == 0)
	{
		SystemClk = 16000000;
	}

	temp = (RCC->CFGR>>4)&0xF;

	if(temp<8)
	{
		ahbp =1;
	}
	else
	{
		ahbp = AHB_PreScaler[temp-8];
	}

	temp = (RCC->CFGR>>10)&0x7;

	if(temp<4)
	{
		apb1p =1;
	}
	else
	{
		apb1p = APB_PreScaler[temp-4];
	}

	pclk = SystemClk/(ahbp*apb1p);

	return pclk;


}








//I2C INIT AND DEINIT API IMPLEMENTATION



void I2C_Init(I2C_Handle_t *pI2CHandle)
{


	uint32_t tempreg = 0;

	//ack control bit
	tempreg |= pI2CHandle-> I2C_Config.I2C_ACKControl<<10;
	pI2CHandle->pI2Cx->CR1 = tempreg;


	//configure the FREQ field of CR2
	tempreg = 0;
	tempreg	|= (RCC_GetPCLK1Value()/1000000U)&0x3F;
	pI2CHandle->pI2Cx->CR2 = tempreg;

	//program device own address
	tempreg =  pI2CHandle->I2C_Config.I2CDeviceAddress<<1;
	tempreg = 1<<14;
	pI2CHandle->pI2Cx->OAR1 = tempreg;

	//ccr values
	uint16_t  ccr_value  = 0;
	tempreg = 0;
	if(pI2CHandle->I2C_Config.I2CSCLSpeed <= I2C_SCL_SPEED_SM)
	{
		// MODE IS STANDARD MODE
		ccr_value = RCC_GetPCLK1Value()/(2*(pI2CHandle->I2C_Config.I2CSCLSpeed));
		tempreg |= (ccr_value & 0xFFF);
	}
	else
	{
		//mode is fast mode
		tempreg |= 1<<15;
		tempreg |= pI2CHandle->I2C_Config.I2C_FMDutyCycle <<14;
		if(pI2CHandle->I2C_Config.I2C_FMDutyCycle == I2C_FM_DUTY_2)
		{
			ccr_value = RCC_GetPCLK1Value()/(3*pI2CHandle->I2C_Config.I2CSCLSpeed);
		}
		else
		{
			ccr_value = RCC_GetPCLK1Value()/(25*pI2CHandle->I2C_Config.I2CSCLSpeed);
		}

		tempreg |=	(ccr_value & 0xFFF);

		pI2CHandle->pI2Cx->CCR |= tempreg;

	}

	//TRISE CONFIGURATION
	if(pI2CHandle->I2C_Config.I2CSCLSpeed <= I2C_SCL_SPEED_SM)
	{
		//standard mode
		tempreg = (RCC_GetPCLK1Value()/1000000U) +1;
	}
	else
	{
		//fast mode
		tempreg = ((RCC_GetPCLK1Value()*300)/1000000000U) +1;
	}
	pI2CHandle->pI2Cx->TRISE |=(tempreg&0x3F);








}

